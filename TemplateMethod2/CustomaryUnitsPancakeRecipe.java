public class CustomaryUnitsPancakeRecipe extends PancakeRecipe {
    void pourFlour() {
        System.out.println("Pour 1 1/2 cups of all-purpose flour");
    }

    void pourBakingPowder() {
        System.out.println("Pour 3 1/2 teaspoons of baking powder");
    }

    void pourSaltAndSugar() {
        System.out.println("Pour 1 teaspoon of salt and 1 tablespoon of white sugar");
    }

    void pourMilk() {
        System.out.println("Pour 1 1/4 of cups milk");
    }

    void pourMeltedButter() {
        System.out.println("Pour 3 tablespoons of butter, melted");
    }
}
